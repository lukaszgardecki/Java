package Zad_10_KonwerterJednostek;

public class UnitConverter {

    double mTOcm(double m) {
        return m * 100;
    }

    double mTOmm(double m) {
        return m * 1000;
    }

    double cmTOm(double cm) {
        return cm / 100;
    }

    double mmTOm(double mm) {
        return mm / 1000;
    }

}
